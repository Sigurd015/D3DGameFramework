#include "pch.h"
#include "DXTrace.h"

void CheckD3DError(HRESULT result)
{
	if (result == S_OK)
		return;

	switch (result)
	{
	case D3D11_ERROR_FILE_NOT_FOUND:
		CORE_ASSERT(false, "D3D11_ERROR_FILE_NOT_FOUND");
		break;
	case D3D11_ERROR_TOO_MANY_UNIQUE_STATE_OBJECTS:
		CORE_ASSERT(false, "D3D11_ERROR_TOO_MANY_UNIQUE_STATE_OBJECTS");
		break;
	case D3D11_ERROR_TOO_MANY_UNIQUE_VIEW_OBJECTS:
		CORE_ASSERT(false, "D3D11_ERROR_TOO_MANY_UNIQUE_VIEW_OBJECTS");
		break;
	case D3D11_ERROR_DEFERRED_CONTEXT_MAP_WITHOUT_INITIAL_DISCARD:
		CORE_ASSERT(false, "D3D11_ERROR_DEFERRED_CONTEXT_MAP_WITHOUT_INITIAL_DISCARD");
		break;
	case DXGI_ERROR_INVALID_CALL:
		CORE_ASSERT(false, "DXGI_ERROR_INVALID_CALL");
		break;
	case DXGI_ERROR_WAS_STILL_DRAWING:
		CORE_ASSERT(false, "DXGI_ERROR_WAS_STILL_DRAWING");
		break;
	case E_FAIL:
		CORE_ASSERT(false, "E_FAIL");
		break;
	case E_INVALIDARG:
		CORE_ASSERT(false, "E_INVALIDARG");
		break;
	case E_OUTOFMEMORY:
		CORE_ASSERT(false, "E_OUTOFMEMORY");
		break;
	case E_NOTIMPL:
		CORE_ASSERT(false, "E_NOTIMPL");
		break;
	case S_FALSE:
		CORE_ASSERT(false, "S_FALSE");
		break;
	}
}